export const MARKETS_GROUPS = marketsGroups.reduce((acc, b) => {
  const markets = {};
  b.groups.forEach(group => {
    group.markets.forEach(
      market => (markets[market.code] = { ...market, group }),
    );
  });
  acc[b.id] = { ...b, markets };
  return acc;
}, {});

export const MARKET_BY_CODE = Object.values(MARKETS_GROUPS).reduce((acc, b) => {
  return { ...acc, ...b.markets };
}, {});

